function __(CODE, EXTRA, IS_ARRAY, FOR_LANGUAGE) {
    var stuff = (EXTRA && EXTRA.length > 0) ? EXTRA : "";
    if(FOR_LANGUAGE && FOR_LANGUAGE.length > 0) {
        return LANG[FOR_LANGUAGE][CODE] + stuff;
    }
    if (getQueryVariable('Lang') == "") {
        return LANG["EN"][CODE] + stuff;
    }
    return (!IS_ARRAY) ? LANG[getQueryVariable('Lang')][CODE] + stuff : LANG[getQueryVariable('Lang')][CODE];
}
